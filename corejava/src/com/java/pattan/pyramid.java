package com.java.pattan;

public class pyramid {

	public static void main(String[] args) {
		int r=6;
		  for(int i=1;i<r;i++) 
		    {
		    	for(int k=1;k<=r-i;k++) {
		    		System.out.print("*");
		    	}
		    	for(int l=2;l<=i;l++) {
		    		System.out.print("*");
		    	}
		    	System.out.println();
	}
	}
}
